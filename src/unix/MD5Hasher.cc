#include "../Common.h"

MD5Hasher::MD5Hasher()
{
}

MD5Hasher::~MD5Hasher()
{
}

void MD5Hasher::Hash(char * Buffer, unsigned int Size)
{
    MD5_CTX Context;
    MD5_Init(&Context);

    MD5_Update(&Context, Buffer, Size);

    MD5_Final((unsigned char *) Buffer, &Context);
}

